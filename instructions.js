// remitida a la Universidad Nacional de Loja, en función de los registros académicos -\
// administrativos disponibles; a la cual se le asignará un puntaje equivalente al 50% de la\
// calificación global.\
// Art. 23.- Tipos de evaluación de capacidades y competencias.- Los procesos de\
// evaluación que implemente la Universidad Nacional de Loja, podrá ser de los siguientes\
