// a) Evaluación general para el ingreso a la universidad.\
// b) Evaluación por campo específico de conocimiento y/o por carrera específica\
// conforme la nomenclatura aprobada por el Consejo de Educación Superior.\
// Art. 24.- Otros parámetros de evaluación.- La Universidad Nacional de Loja, en ejercicio\
// de su autonomía responsable, además de la evaluación de capacidadesy competencias, podrá\
// establecer otros parámetros de valoración (entrevista, trabajo académico, entre otros).\
// Art. 25.- Evaluación de capacidades y competencias.- Para comprobar el nivel de\
// capacidades y competencias de las y los aspirantes, se aplicará un formulario de cien\
